#include "network_sort.h"
#include <stdlib.h>

#define COMPARE_SWAP(a, b) {uint16_t x = base[a]; uint16_t y = base[b]; base[a] = x > y ? y : x; base[b] = x > y ? x : y;}

void network_sort(uint16_t* base, int length, int (*compare)(const void*, const void*)) {
	switch(length) {
		case 11:
			network_sort11(base);
			break;
		case 12:
			network_sort12(base);
			break;
		case 13:
			network_sort13(base);
			break;
		case 14:
			network_sort14(base);
			break;
		case 15:
			network_sort15(base);
			break;
		case 16:
			network_sort16(base);
			break;
		case 17:
			network_sort17(base);
			break;
		case 18:
			network_sort18(base);
			break;
		case 19:
			network_sort19(base);
			break;
		case 20:
			network_sort20(base);
			break;
		case 21:
			network_sort21(base);
			break;
		case 22:
			network_sort22(base);
			break;
		case 23:
			network_sort23(base);
			break;
		case 24:
			network_sort24(base);
			break;
		default:
			qsort(base, length, sizeof(uint16_t), compare);
	}
}

